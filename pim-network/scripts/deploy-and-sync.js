const fs = require('fs');
const path = require('path');

async function main() {
  const [deployer] = await ethers.getSigners();
  console.log('Deployer:', deployer.address);

  const Token = await ethers.getContractFactory('PIMToken');
  const token = await Token.deploy();
  await token.deployed();

  console.log('PIMToken deployed:', token.address);

  const target = path.join(__dirname, '..', '..', 'pim-ar-wallet', 'src', 'config', 'pimToken.js');
  const content = `// Auto-generated by pim-network/scripts/deploy-and-sync.js\n// Chain 31337 = Hardhat Local\n\nexport const PIM_TOKEN_BY_CHAIN = {\n  31337: '${token.address}',\n};\n\nexport function pimTokenAddressForChain(chainId) {\n  const addr = PIM_TOKEN_BY_CHAIN[chainId];\n  if (!addr) return null;\n  return addr;\n}\n`;

  fs.writeFileSync(target, content, 'utf8');
  console.log('Synced frontend config:', target);
}

main().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});
